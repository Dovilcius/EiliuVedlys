package com.example.eiliuvedlys;

public class Priminimas {
    private int id;
    private int projektoId;
    private int eile;
    private String tekstas;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getProjektoId() {
        return projektoId;
    }

    public void setProjektoId(int projektoId) {
        this.projektoId = projektoId;
    }

    public int getEile() {
        return eile;
    }

    public void setEile(int eile) {
        this.eile = eile;
    }

    public String getTekstas() {
        return tekstas;
    }

    public void setTekstas(String tekstas) {
        this.tekstas = tekstas;
    }
}